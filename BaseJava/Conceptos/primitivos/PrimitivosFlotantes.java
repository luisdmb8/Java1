package primitivos;

public class PrimitivosFlotantes {
	public static void main(String[] args) {
		/*
		 tipos primitivos flotantes : float (decimales mas peque√±o) y double (decimales mas grandes)
		 */
		
		float numeroFloat = 10.0f; //float numeroFloat = (float)10.0;
		System.out.println("Valor Float: " + numeroFloat);
		System.out.println("Valor minimo Float:" + Float.MIN_VALUE);
		System.out.println("Valor maximo Float:" + Float.MAX_VALUE);
		
		double numeroDouble=10;
		System.out.println("Valor Double: " + numeroDouble);
		System.out.println("Valor minimo Double:" + Double.MIN_VALUE);
		System.out.println("Valor maximo Double:" + Double.MAX_VALUE);
		
	}

}
