package primitivos;

public class UsoDebug {
	public static void main(String[] args) {
		
		// debug en el codigo con F6 va linea por linea y F8 lo ejecuta todo
		int numeroEntero = 10 ;
		System.out.println("Numero Entero: " + numeroEntero);
		
		float numeroFloat = 10.0f;
		System.out.println("Numero Flotante: " + numeroFloat);
		
		double numeroDouble = 10.0;
		System.out.println("Numero Double: " + numeroDouble);
		
		// se selecciona las lineas que se le quieren hacer un Debug y se le debug
		
	}

}
